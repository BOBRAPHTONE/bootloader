#include "bootlogic.h"
#include "reboot.h"
#include "ota.h"

void boot_ota(enum wake_sources		__unused(wake_source),
	      enum reset_reasons	__unused(reset_reason),
	      enum boot_targets		__unused(boot_target))
{
#if defined(CONFIG_OTA)

	set_boot_flags(BOOT_OTA_ONGOING);

	if (ota_start())
		set_boot_flags(BOOT_OTA_FAILURE);
	else
		set_boot_flags(BOOT_OTA_COMPLETE);

	reboot(TARGET_MAIN);
#endif
}
